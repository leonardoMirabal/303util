#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ARTIFACTS_DIR="${ARTIFACTS_DIR:-${SCRIPT_DIR}/artifacts}"
WEB_ARTIFACT_DIR="${ARTIFACTS_DIR}/web"
APK_ARTIFACT_DIR="${ARTIFACTS_DIR}/apk"
RELEASE_VERSION="${RELEASE_VERSION:-$(node -p "require(process.argv[1]).version" "${SCRIPT_DIR}/package.json")}"
RELEASE_VERSION="${RELEASE_VERSION#v}"
ANDROID_BUILD_MODE="debug"
ANDROID_BUILD_ARGS=(--apk --debug --ci)
ANDROID_KEYSTORE_FILE=""
ANDROID_KEYSTORE_PATH_RESOLVED=""
ANDROID_APKSIGNER=""

cleanup_android_signing() {
  if [ -n "${ANDROID_KEYSTORE_FILE}" ] && [ -f "${ANDROID_KEYSTORE_FILE}" ]; then
    rm -f "${ANDROID_KEYSTORE_FILE}"
  fi

  if [ -f "${SCRIPT_DIR}/src-tauri/gen/android/keystore.properties" ]; then
    rm -f "${SCRIPT_DIR}/src-tauri/gen/android/keystore.properties"
  fi
}

trap cleanup_android_signing EXIT

write_android_version_properties() {
  local version_core prerelease_suffix prerelease_number android_version_code properties_file

  version_core="${RELEASE_VERSION%%-*}"
  if [[ ! "${version_core}" =~ ^([0-9]+)\.([0-9]+)\.([0-9]+)$ ]]; then
    echo "Error: RELEASE_VERSION must be semantic version-like, got ${RELEASE_VERSION}"
    exit 1
  fi

  local major="${BASH_REMATCH[1]}"
  local minor="${BASH_REMATCH[2]}"
  local patch="${BASH_REMATCH[3]}"

  prerelease_suffix=""
  prerelease_number=99
  if [[ "${RELEASE_VERSION}" == *-* ]]; then
    prerelease_suffix="${RELEASE_VERSION#*-}"
    prerelease_number=0
    if [[ "${prerelease_suffix}" =~ \.([0-9]+)$ ]]; then
      prerelease_number="${BASH_REMATCH[1]}"
    fi
  fi

  android_version_code=$((major * 1000000 + minor * 10000 + patch * 100 + prerelease_number))
  properties_file="${SCRIPT_DIR}/src-tauri/gen/android/app/tauri.properties"

  cat > "${properties_file}" <<EOF
# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.
tauri.android.versionName=${RELEASE_VERSION}
tauri.android.versionCode=${android_version_code}
EOF
}

resolve_apksigner() {
  if [ -n "${ANDROID_APKSIGNER}" ]; then
    return
  fi

  if command -v apksigner >/dev/null 2>&1; then
    ANDROID_APKSIGNER="$(command -v apksigner)"
    return
  fi

  local sdk_root="${ANDROID_HOME:-${ANDROID_SDK_ROOT:-$HOME/Library/Android/sdk}}"
  mapfile -t apksigner_candidates < <(find "${sdk_root}/build-tools" -type f -name apksigner 2>/dev/null | sort -Vr)
  if [ "${#apksigner_candidates[@]}" -eq 0 ]; then
    echo "Error: apksigner was not found."
    exit 1
  fi

  ANDROID_APKSIGNER="${apksigner_candidates[0]}"
}

configure_android_signing() {
  if [ -n "${ANDROID_KEY_ALIAS:-}" ] && [ -n "${ANDROID_KEY_PASSWORD:-}" ] && [ -n "${ANDROID_KEY_BASE64:-}" ]; then
    ANDROID_BUILD_MODE="release"
    ANDROID_BUILD_ARGS=(--apk --ci)
    ANDROID_KEYSTORE_FILE="$(mktemp "${ARTIFACTS_DIR}/android-keystore.XXXXXX.jks")"
    printf '%s' "${ANDROID_KEY_BASE64}" | base64 --decode > "${ANDROID_KEYSTORE_FILE}"
    ANDROID_KEYSTORE_PATH_RESOLVED="${ANDROID_KEYSTORE_FILE}"
    cat > "${SCRIPT_DIR}/src-tauri/gen/android/keystore.properties" <<EOF
keyAlias=${ANDROID_KEY_ALIAS}
password=${ANDROID_KEY_PASSWORD}
storeFile=${ANDROID_KEYSTORE_PATH_RESOLVED}
EOF
    echo "Android signing configured for release APKs."
  elif [ -n "${ANDROID_KEY_ALIAS:-}" ] && [ -n "${ANDROID_KEY_PASSWORD:-}" ] && [ -n "${ANDROID_KEYSTORE_PATH:-}" ]; then
    if [ ! -f "${ANDROID_KEYSTORE_PATH}" ]; then
      echo "Error: ANDROID_KEYSTORE_PATH does not exist: ${ANDROID_KEYSTORE_PATH}"
      exit 1
    fi
    ANDROID_BUILD_MODE="release"
    ANDROID_BUILD_ARGS=(--apk --ci)
    ANDROID_KEYSTORE_PATH_RESOLVED="${ANDROID_KEYSTORE_PATH}"
    cat > "${SCRIPT_DIR}/src-tauri/gen/android/keystore.properties" <<EOF
keyAlias=${ANDROID_KEY_ALIAS}
password=${ANDROID_KEY_PASSWORD}
storeFile=${ANDROID_KEYSTORE_PATH_RESOLVED}
EOF
    echo "Android signing configured for release APKs from local keystore."
  else
    echo "Android signing secrets not set. Building installable debug APK."
  fi
}

enforce_android_landscape() {
  local manifest_file="${SCRIPT_DIR}/src-tauri/gen/android/app/src/main/AndroidManifest.xml"

  if [ ! -f "${manifest_file}" ]; then
    echo "Error: Android manifest not found at ${manifest_file}"
    exit 1
  fi

  if grep -q 'android:screenOrientation=' "${manifest_file}"; then
    perl -0pi -e 's/android:screenOrientation="[^"]*"/android:screenOrientation="sensorLandscape"/g' "${manifest_file}"
    return
  fi

  perl -0pi -e 's/(android:name="\.MainActivity"\n)/$1            android:screenOrientation="sensorLandscape"\n/' "${manifest_file}"
}

if [ -z "${VITE_GOOGLE_CLIENT_ID:-}" ]; then
  echo "Error: VITE_GOOGLE_CLIENT_ID is required."
  exit 1
fi

mkdir -p "${ARTIFACTS_DIR}"
rm -rf "${WEB_ARTIFACT_DIR}" "${APK_ARTIFACT_DIR}"
mkdir -p "${WEB_ARTIFACT_DIR}" "${APK_ARTIFACT_DIR}"

echo "Building GitHub Pages bundle..."
bash "${SCRIPT_DIR}/prepare-dev-index.sh"
GH_PAGES=true npm run build
cp -R "${SCRIPT_DIR}/dist/." "${WEB_ARTIFACT_DIR}/"
(
  cd "${ARTIFACTS_DIR}"
  rm -f web-bundle.zip
  zip -qry web-bundle.zip web
)

echo "Building Android APK..."
if [ ! -d "${SCRIPT_DIR}/src-tauri/gen/android" ]; then
  echo "Initializing Android project..."
  npx tauri android init --ci --skip-targets-install
fi

enforce_android_landscape
write_android_version_properties
configure_android_signing
npx tauri android build "${ANDROID_BUILD_ARGS[@]}"

mapfile -t apk_files < <(find "${SCRIPT_DIR}/src-tauri/gen/android/app/build/outputs/apk" -type f -name "*.apk" | sort)

if [ "${#apk_files[@]}" -eq 0 ]; then
  echo "Error: No APK files were generated."
  exit 1
fi

copied_apk_count=0
for apk_file in "${apk_files[@]}"; do
  apk_name="$(basename "${apk_file}")"
  apk_name="${apk_name#app-}"
  if [[ "${apk_file}" == *-unsigned.apk ]]; then
    if [ "${ANDROID_BUILD_MODE}" != "release" ]; then
      continue
    fi
    resolve_apksigner
    apk_name="${apk_name/-unsigned/}"
    if [[ "${apk_name}" != 303util* ]]; then
      apk_name="303util-${RELEASE_VERSION}-${apk_name}"
    fi
    "${ANDROID_APKSIGNER}" sign \
      --ks "${ANDROID_KEYSTORE_PATH_RESOLVED}" \
      --ks-key-alias "${ANDROID_KEY_ALIAS}" \
      --ks-pass "pass:${ANDROID_KEY_PASSWORD}" \
      --key-pass "pass:${ANDROID_KEY_PASSWORD}" \
      --out "${APK_ARTIFACT_DIR}/${apk_name}" \
      "${apk_file}"
    copied_apk_count=$((copied_apk_count + 1))
    continue
  fi
  if [[ "${apk_name}" != 303util* ]]; then
    apk_name="303util-${RELEASE_VERSION}-${apk_name}"
  fi
  cp "${apk_file}" "${APK_ARTIFACT_DIR}/${apk_name}"
  copied_apk_count=$((copied_apk_count + 1))
done

if [ "${copied_apk_count}" -eq 0 ]; then
  echo "Error: No installable APK files were found after the ${ANDROID_BUILD_MODE} build."
  exit 1
fi

echo "Artifacts ready in ${ARTIFACTS_DIR}"
